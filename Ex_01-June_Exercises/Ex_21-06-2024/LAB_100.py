# Dict
# Key and Value
# name -> "Jyoti"

# A dictionary is an unordered collection of data
# in a key-value pair format.

# Declare dictionary
my_dict_2 = {
    "name" : "Jyoti",
    "age" : 23,
    "address" : "Ahmedabad"
}

print(my_dict_2)
print(len(my_dict_2))

my_dict_2["name"] = "Shashi"
print(my_dict_2)

print(my_dict_2["age"])
print(my_dict_2["address"])


# Declare dictionary
phone_book = dict(name = "Amit", age = 57, address = "Delhi")
