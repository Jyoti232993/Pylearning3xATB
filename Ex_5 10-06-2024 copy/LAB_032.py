# Ternary Operator
jyoti_marks = 90
karan_marks = 97

#   x > y -> do something - print("pramod")
# y > x -> do something else -> print("amit)

print("Jyoti is winner" if jyoti_marks > karan_marks else "karan is winner")

if (jyoti_marks > karan_marks):
    print("Jyoti is winner")
else:
    print("Karan is winner")